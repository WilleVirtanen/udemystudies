package Day5;

public class DoWhileLoop 
{
    public static void main(String[] args) 
    {
        int i=1;
        do
        {
            System.out.println("Hello!" +i);
            i++;
        }
        while(i<=10);

        int i2=10;
        do
        {
            System.out.println("Hello" +i2);
            i2--;
        }
        while(i2>=1);
    }

}
